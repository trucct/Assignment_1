const express = require('express');
const appared_sizeController = require('../../controllers/appared_size.controller');

const router = express.Router();

router
  .route('/')
  .post(appared_sizeController.createAppared_size)
  .get(appared_sizeController.getAppared_sizes);

router
  .route('/:appared_sizeId')
  .get(appared_sizeController.getAppared_size)
  .patch(appared_sizeController.updateAppared_size)
  .delete(appared_sizeController.deleteAppared_size);

module.exports = router;

